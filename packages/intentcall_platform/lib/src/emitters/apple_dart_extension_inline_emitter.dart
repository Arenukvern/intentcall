import '../agent_manifest.dart';
import 'emitter_utils.dart';

/// Emits experimental App Intents extension scaffolds for Dart inline runtime.
///
/// This emitter is intentionally not wired into normal [PlatformSync]. It
/// generates source templates for a separate App Intents extension target, but
/// does not claim live OS invocation, plugin compatibility, or extension target
/// membership proof.
final class AppleDartExtensionInlineEmitter {
  const AppleDartExtensionInlineEmitter({
    this.enableExperimental = false,
    this.channelName = 'dev.intentcall/dart_extension_inline',
    this.dartEntrypoint = 'intentCallDartExtensionInlineMain',
    this.extensionTypeName = 'IntentCallDartExtensionInlineExtension',
  });

  final bool enableExperimental;
  final String channelName;
  final String dartEntrypoint;
  final String extensionTypeName;

  String emitSwiftExtension(final AgentManifest manifest) {
    final tools = _dartExtensionInlineTools(manifest).toList();
    if (tools.isNotEmpty && !enableExperimental) {
      throw UnsupportedError(
        'Apple dartExtensionInline scaffold is experimental. Re-run with '
        'enableExperimental: true only for a proof app with an App Intents '
        'extension target, FlutterEngine bootstrap audit, plugin allowlist, '
        'App Group or IPC plan, timeout/memory limits, and live invocation '
        'evidence.',
      );
    }

    final buffer = StringBuffer()
      ..writeln('// Generated by intentcall_platform — experimental scaffold.')
      ..writeln(
        '// Do not claim support until a fixture extension proves live OS invocation.',
      )
      ..writeln('import AppIntents')
      ..writeln('import Foundation')
      ..writeln('#if os(macOS)')
      ..writeln('import FlutterMacOS')
      ..writeln('#else')
      ..writeln('import Flutter')
      ..writeln('#endif')
      ..writeln()
      ..writeln('@main')
      ..writeln('struct $extensionTypeName: AppIntentsExtension {}')
      ..writeln();

    for (final tool in tools) {
      _writeSwiftIntent(buffer, tool);
    }

    buffer
      ..writeln('struct IntentCallDartExtensionInlineResult {')
      ..writeln('  let ok: Bool')
      ..writeln('  let dialog: String')
      ..writeln('  let code: String?')
      ..writeln('  let data: [String: Any]')
      ..writeln('}')
      ..writeln()
      ..writeln(
        'enum IntentCallDartExtensionInlineError: Error, CustomStringConvertible {',
      )
      ..writeln('  case engineStartFailed')
      ..writeln('  case invalidResponse')
      ..writeln('  case runtimeFailure(String)')
      ..writeln('  case invalidTypedResult(String, String, String)')
      ..writeln()
      ..writeln('  var description: String {')
      ..writeln('    switch self {')
      ..writeln('    case .engineStartFailed:')
      ..writeln(
        '      return "Dart extension inline runtime failed to start FlutterEngine."',
      )
      ..writeln('    case .invalidResponse:')
      ..writeln(
        '      return "Dart extension inline runtime returned an invalid response."',
      )
      ..writeln('    case .runtimeFailure(let message):')
      ..writeln('      return message')
      ..writeln(
        '    case .invalidTypedResult(let qualifiedName, let dataKey, let typeName):',
      )
      ..writeln(
        r'      return "Dart extension inline runtime for \(qualifiedName) did not return \(typeName) at data.\(dataKey)."',
      )
      ..writeln('    }')
      ..writeln('  }')
      ..writeln('}')
      ..writeln()
      ..writeln('enum IntentCallDartExtensionInlineRuntime {')
      ..writeln('  private static let lock = NSObject()')
      ..writeln(
        '  private static let channelName = "${escapeSwiftString(channelName)}"',
      )
      ..writeln(
        '  private static let dartEntrypoint = "${escapeSwiftString(dartEntrypoint)}"',
      )
      ..writeln(
        '  private nonisolated(unsafe) static var engine: FlutterEngine?',
      )
      ..writeln()
      ..writeln(
        '  static func perform(qualifiedName: String, arguments: [String: Any]) async throws -> IntentCallDartExtensionInlineResult {',
      )
      ..writeln('    let engine = try bootEngine()')
      ..writeln(
        '    let channel = FlutterMethodChannel(name: channelName, binaryMessenger: engine.binaryMessenger)',
      )
      ..writeln('    let envelope: [String: Any] = [')
      ..writeln('      "id": UUID().uuidString,')
      ..writeln('      "qualifiedName": qualifiedName,')
      ..writeln('      "arguments": arguments,')
      ..writeln('      "source": "apple.dart_extension_inline",')
      ..writeln(
        '      "createdAt": ISO8601DateFormatter().string(from: Date())',
      )
      ..writeln('    ]')
      ..writeln(
        '    return try await withCheckedThrowingContinuation { continuation in',
      )
      ..writeln(
        '      channel.invokeMethod("perform", arguments: envelope) { response in',
      )
      ..writeln('        guard let map = response as? [String: Any] else {')
      ..writeln(
        '          continuation.resume(throwing: IntentCallDartExtensionInlineError.invalidResponse)',
      )
      ..writeln('          return')
      ..writeln('        }')
      ..writeln('        let ok = map["ok"] as? Bool ?? false')
      ..writeln(
        '        let dialog = map["dialog"] as? String ?? map["message"] as? String ?? ""',
      )
      ..writeln('        let code = map["code"] as? String')
      ..writeln('        let data = map["data"] as? [String: Any] ?? [:]')
      ..writeln(
        '        continuation.resume(returning: IntentCallDartExtensionInlineResult(ok: ok, dialog: dialog, code: code, data: data))',
      )
      ..writeln('      }')
      ..writeln('    }')
      ..writeln('  }')
      ..writeln()
      ..writeln('  private static func bootEngine() throws -> FlutterEngine {')
      ..writeln('    objc_sync_enter(lock)')
      ..writeln('    defer { objc_sync_exit(lock) }')
      ..writeln('    if let engine { return engine }')
      ..writeln(
        '    let next = FlutterEngine(name: "intentcall_dart_extension_inline")',
      )
      ..writeln('    guard next.run(withEntrypoint: dartEntrypoint) else {')
      ..writeln(
        '      throw IntentCallDartExtensionInlineError.engineStartFailed',
      )
      ..writeln('    }')
      ..writeln(
        '    IntentCallDartExtensionPluginRegistrant.registerAllowedPlugins(with: next)',
      )
      ..writeln('    engine = next')
      ..writeln('    return next')
      ..writeln('  }')
      ..writeln()
      ..writeln(
        '  static func typedValue<T>(_ result: IntentCallDartExtensionInlineResult, dataKey: String, as type: T.Type, qualifiedName: String) throws -> T {',
      )
      ..writeln('    let raw = result.data[dataKey]')
      ..writeln('    if let value = raw as? T {')
      ..writeln('      return value')
      ..writeln('    }')
      ..writeln('    if T.self == Int.self, let value = raw as? NSNumber {')
      ..writeln('      return value.intValue as! T')
      ..writeln('    }')
      ..writeln('    if T.self == Double.self, let value = raw as? NSNumber {')
      ..writeln('      return value.doubleValue as! T')
      ..writeln('    }')
      ..writeln('    if T.self == Bool.self, let value = raw as? NSNumber {')
      ..writeln('      return value.boolValue as! T')
      ..writeln('    }')
      ..writeln(
        '    throw IntentCallDartExtensionInlineError.invalidTypedResult(qualifiedName, dataKey, String(describing: T.self))',
      )
      ..writeln('  }')
      ..writeln('}')
      ..writeln()
      ..writeln('enum IntentCallDartExtensionPluginRegistrant {')
      ..writeln(
        '  static func registerAllowedPlugins(with engine: FlutterEngine) {',
      )
      ..writeln(
        '    // Intentionally empty. Add only audited extension-safe plugins here.',
      )
      ..writeln('  }')
      ..writeln('}');

    return buffer.toString();
  }

  String emitDartEntrypointTemplate() =>
      '''
// Generated by intentcall_platform — experimental scaffold.
// Copy into the Flutter app, replace buildIntentCallDartExtensionRegistry(),
// audit plugins, and prove live App Intents extension invocation before shipping.
import 'package:flutter/services.dart';
import 'package:flutter/widgets.dart';
import 'package:intentcall_core/intentcall_core.dart';
import 'package:intentcall_platform/intentcall_platform.dart';

@pragma('vm:entry-point')
void $dartEntrypoint() {
  WidgetsFlutterBinding.ensureInitialized();
  const channel = MethodChannel('$channelName');
  final runtime = IntentCallDartExtensionInlineRuntime.bindRegistry(
    registry: buildIntentCallDartExtensionRegistry(),
    policy: const IntentCallAuthorizationPolicy.denyAll(),
  );
  channel.setMethodCallHandler((call) async {
    if (call.method != 'perform') {
      return <String, Object?>{
        'ok': false,
        'code': 'unsupported_method',
        'message': 'Unsupported dartExtensionInline method: \${call.method}',
        'dialog': 'Unsupported dartExtensionInline method.',
      };
    }
    return runtime.perform(call.arguments);
  });
}

AgentRegistry buildIntentCallDartExtensionRegistry() {
  throw UnsupportedError(
    'Register an extension-safe AgentRegistry and explicit authorization policy '
    'before enabling dartExtensionInline.',
  );
}
''';

  Iterable<AgentManifestEntry> _dartExtensionInlineTools(
    final AgentManifest manifest,
  ) sync* {
    for (final tool in manifest.tools) {
      final inlineRuntime = tool.inlineRuntime;
      if (tool.dispatchMode != AgentManifestDispatchMode.inlineRuntime ||
          inlineRuntime?.kind !=
              AgentManifestInlineRuntimeKind.dartExtensionInline) {
        continue;
      }
      final apple = inlineRuntime?.platforms.apple;
      if (apple == null) {
        throw UnsupportedError(
          'Apple dartExtensionInline dispatch for ${tool.qualifiedName} '
          'requires inlineRuntime.platforms.apple.',
        );
      }
      if (apple.target !=
          AgentManifestAppleInlineRuntimeTarget.appIntentsExtension) {
        throw UnsupportedError(
          'Apple dartExtensionInline requires target "appIntentsExtension" '
          'for ${tool.qualifiedName}.',
        );
      }
      yield tool;
    }
  }

  void _writeSwiftIntent(
    final StringBuffer buffer,
    final AgentManifestEntry tool,
  ) {
    final typeName = swiftIntentTypeName(tool.qualifiedName);
    final title = escapeSwiftString(
      tool.description.isEmpty
          ? humanizeAgentName(tool.name)
          : tool.description,
    );
    final parameters = _swiftParameters(tool);
    final typedResult = tool.inlineRuntime?.result;
    buffer
      ..writeln('@available(iOS 16.0, macOS 13.0, *)')
      ..writeln('struct $typeName: AppIntent {')
      ..writeln('  static var title: LocalizedStringResource = "$title"')
      ..writeln('  @available(iOS 26.0, macOS 26.0, *)')
      ..writeln('  static var supportedModes: IntentModes { .background }')
      ..writeln('  @available(iOS 27.0, macOS 27.0, *)')
      ..writeln(
        '  static var allowedExecutionTargets: IntentExecutionTargets { .appIntentsExtension }',
      )
      ..writeln();
    for (final parameter in parameters) {
      buffer
        ..writeln(
          '  @Parameter(title: "${escapeSwiftString(parameter.title)}")',
        )
        ..writeln('  var ${parameter.variableName}: ${parameter.swiftType}')
        ..writeln();
    }
    buffer
      ..writeln(
        '  func perform() async throws -> ${_swiftPerformResultType(typedResult)} {',
      )
      ..writeln('    var arguments: [String: Any] = [:]');
    for (final line in _swiftArgumentLines(parameters)) {
      buffer.writeln('    $line');
    }
    if (typedResult == null) {
      buffer
        ..writeln('    do {')
        ..writeln(
          '      let result = try await IntentCallDartExtensionInlineRuntime.perform(qualifiedName: "${escapeSwiftString(tool.qualifiedName)}", arguments: arguments)',
        )
        ..writeln('      if result.ok {')
        ..writeln(
          '        return .result(dialog: IntentDialog(stringLiteral: result.dialog))',
        )
        ..writeln('      }')
        ..writeln(
          r'      return .result(dialog: IntentDialog("Dart extension inline failed: \(result.dialog)"))',
        )
        ..writeln(
          '    } catch let error as IntentCallDartExtensionInlineError {',
        )
        ..writeln(
          '      return .result(dialog: IntentDialog(stringLiteral: error.description))',
        )
        ..writeln('    } catch {')
        ..writeln(
          r'      return .result(dialog: IntentDialog("Dart extension inline failed: \(error.localizedDescription)"))',
        )
        ..writeln('    }');
    } else {
      final swiftType = _swiftTypeForResult(typedResult.type);
      buffer
        ..writeln(
          '    let result = try await IntentCallDartExtensionInlineRuntime.perform(qualifiedName: "${escapeSwiftString(tool.qualifiedName)}", arguments: arguments)',
        )
        ..writeln('    if !result.ok {')
        ..writeln(
          '      throw IntentCallDartExtensionInlineError.runtimeFailure(result.dialog)',
        )
        ..writeln('    }')
        ..writeln(
          '    let value = try IntentCallDartExtensionInlineRuntime.typedValue(result, dataKey: "${escapeSwiftString(typedResult.dataKey)}", as: $swiftType.self, qualifiedName: "${escapeSwiftString(tool.qualifiedName)}")',
        )
        ..writeln(
          '    return .result(value: value, dialog: IntentDialog(stringLiteral: result.dialog))',
        );
    }
    buffer
      ..writeln('  }')
      ..writeln('}')
      ..writeln();
  }
}

final class _SwiftParameter {
  const _SwiftParameter({
    required this.name,
    required this.variableName,
    required this.title,
    required this.swiftType,
    required this.required,
  });

  final String name;
  final String variableName;
  final String title;
  final String swiftType;
  final bool required;
}

List<_SwiftParameter> _swiftParameters(final AgentManifestEntry tool) {
  final properties = tool.inputSchema['properties'];
  if (properties is! Map) {
    return const <_SwiftParameter>[];
  }
  final rawRequired = tool.inputSchema['required'];
  final required = <String>{
    ...(rawRequired is List ? rawRequired : const <Object?>[])
        .whereType<String>(),
  };
  final out = <_SwiftParameter>[];
  for (final entry in properties.entries) {
    final name = '${entry.key}';
    final schema = entry.value;
    if (schema is! Map) {
      continue;
    }
    final schemaType = '${schema['type']}';
    final baseType = _swiftTypeFor(schemaType);
    if (baseType == null) {
      throw UnsupportedError(
        'Apple App Intents support only primitive string/integer/number/boolean '
        'parameters in ${tool.qualifiedName}; "$name" has unsupported type '
        '"$schemaType".',
      );
    }
    final isRequired = required.contains(name);
    out.add(
      _SwiftParameter(
        name: name,
        variableName: _swiftIdentifier(name),
        title: humanizeAgentName(name),
        swiftType: isRequired ? baseType : '$baseType?',
        required: isRequired,
      ),
    );
  }
  return out;
}

List<String> _swiftArgumentLines(final List<_SwiftParameter> parameters) {
  final out = <String>[];
  for (final parameter in parameters) {
    if (parameter.required) {
      out.add(
        'arguments["${escapeSwiftString(parameter.name)}"] = ${parameter.variableName}',
      );
    } else {
      out.add(
        'if let value = ${parameter.variableName} { arguments["${escapeSwiftString(parameter.name)}"] = value }',
      );
    }
  }
  return out;
}

String? _swiftTypeFor(final String type) => switch (type) {
  'string' => 'String',
  'integer' => 'Int',
  'number' => 'Double',
  'boolean' => 'Bool',
  _ => null,
};

String _swiftPerformResultType(final AgentManifestInlineRuntimeResult? result) {
  if (result == null) {
    return 'some IntentResult';
  }
  return 'some IntentResult & ReturnsValue<${_swiftTypeForResult(result.type)}> & ProvidesDialog';
}

String _swiftTypeForResult(final AgentManifestInlineRuntimeResultType type) =>
    switch (type) {
      AgentManifestInlineRuntimeResultType.string => 'String',
      AgentManifestInlineRuntimeResultType.integer => 'Int',
      AgentManifestInlineRuntimeResultType.number => 'Double',
      AgentManifestInlineRuntimeResultType.boolean => 'Bool',
    };

String _swiftIdentifier(final String name) {
  final parts = name
      .split(RegExp('[^A-Za-z0-9]+'))
      .where((final p) => p.isNotEmpty);
  if (parts.isEmpty) {
    return 'value';
  }
  final first = parts.first;
  final rest = parts
      .skip(1)
      .map((final part) => '${part[0].toUpperCase()}${part.substring(1)}');
  final candidate = first + rest.join();
  final identifier = RegExp('^[A-Za-z_]').hasMatch(candidate)
      ? candidate
      : 'value$candidate';
  return _swiftReservedWords.contains(identifier)
      ? '`$identifier`'
      : identifier;
}

