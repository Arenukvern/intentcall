import '../agent_manifest.dart';
import 'emitter_utils.dart';

/// Emits a Windows registry script for app-owned protocol activation.
final class WindowsProtocolEmitter {
  const WindowsProtocolEmitter({
    this.protocolScheme,
    this.exePlaceholder = '@APP_EXE@',
  });

  final String? protocolScheme;
  final String exePlaceholder;

  String emit(final AgentManifest manifest) {
    final scheme = requireProtocolScheme(
      protocolScheme ?? manifest.protocolScheme,
      artifact: 'Windows protocol registry',
    );
    final toolsComment = manifest.tools
        .where(
          (final tool) => tool.surfaces.includes(
            AgentManifestSurface.windowsProtocolActivation,
            defaultValue: false,
          ),
        )
        .map((final t) => '; tool: ${t.qualifiedName}')
        .join('\n');
    return '''
; Generated by intentcall_platform — do not edit by hand.
$toolsComment
Windows Registry Editor Version 5.00

[HKEY_CURRENT_USER\\Software\\Classes\\$scheme]
@="URL:IntentCall Protocol"
"URL Protocol"=""

[HKEY_CURRENT_USER\\Software\\Classes\\$scheme\\shell\\open\\command]
@="\\"$exePlaceholder\\" \\"%1\\""
''';
  }

  /// MSIX `Extensions` fragment for protocol activation (optional packaging).
  String emitMsixFragment(final AgentManifest manifest) {
    final scheme = requireProtocolScheme(
      protocolScheme ?? manifest.protocolScheme,
      artifact: 'Windows MSIX protocol fragment',
    );
    final names = manifest.tools
        .where(
          (final tool) => tool.surfaces.includes(
            AgentManifestSurface.windowsMsixProtocol,
            defaultValue: false,
          ),
        )
        .map((final t) => t.qualifiedName)
        .join(', ');
    return '''
<!-- Generated by intentcall_platform — do not edit by hand. -->
<!-- tools: $names -->
<uap:Extension Category="windows.protocol">
  <uap:Protocol Name="$scheme">
    <uap:DisplayName>IntentCall</uap:DisplayName>
  </uap:Protocol>
</uap:Extension>
''';
  }
}
