import '../agent_manifest.dart';
import 'emitter_utils.dart';

/// Emits an Apple 27+ AppIntentsTesting UI-test scaffold.
///
/// This is the live-system proof lane: consuming apps add the emitted source to
/// an XCTest UI test target, install/launch the app, and run intents through the
/// App Intents testing infrastructure instead of calling `perform()` directly.
final class AppleAppIntentsTestingEmitter {
  const AppleAppIntentsTestingEmitter({
    required this.bundleIdentifier,
    this.testClassName = 'IntentCallAppIntentsLiveInvocationTests',
    this.sampleArguments = const <String, Map<String, Object?>>{},
    this.entityFixtures = const <String, AppleAppIntentsTestingEntityFixture>{},
  });

  final String bundleIdentifier;
  final String testClassName;

  /// Sample primitive parameter values keyed by manifest `qualifiedName`.
  ///
  /// Required App Intent parameters must be present here for generated live
  /// invocation tests to be runnable in a consuming app.
  final Map<String, Map<String, Object?>> sampleArguments;

  /// Sample entity values keyed by manifest entity `qualifiedName`.
  ///
  /// Entity tests are emitted only for entity types with fixtures. This keeps
  /// the generated scaffold opt-in for apps that have seeded searchable entity
  /// data in their UI-test environment.
  final Map<String, AppleAppIntentsTestingEntityFixture> entityFixtures;

  String emitUiTests(final AgentManifest manifest) {
    final tools = manifest.tools.toList();
    final entityTypes = manifest.entityTypes
        .where(
          (final entityType) =>
              entityFixtures.containsKey(entityType.qualifiedName),
        )
        .toList();
    if (tools.isEmpty && entityTypes.isEmpty) {
      throw StateError(
        'Apple App Intents testing requires at least one tool or entity '
        'fixture.',
      );
    }

    final buffer = StringBuffer()
      ..writeln('// Generated by intentcall_platform — live proof scaffold.')
      ..writeln('// Add to an XCTest UI test target and run on macOS/iOS 27+.')
      ..writeln('#if canImport(AppIntentsTesting)')
      ..writeln('import AppIntentsTesting')
      ..writeln('import XCTest')
      ..writeln()
      ..writeln('@available(iOS 27.0, macOS 27.0, *)')
      ..writeln('final class $testClassName: XCTestCase {')
      ..writeln('  private var definitions: IntentDefinitions!')
      ..writeln()
      ..writeln('  override func setUp() async throws {')
      ..writeln('    try await super.setUp()')
      ..writeln('    continueAfterFailure = false')
      ..writeln('    await XCUIApplication().launch()')
      ..writeln(
        '    definitions = IntentDefinitions(bundleIdentifier: "${escapeSwiftString(bundleIdentifier)}")',
      )
      ..writeln('  }');

    for (final tool in tools) {
      _writeToolTest(buffer, tool);
    }
    for (final entityType in entityTypes) {
      _writeEntityTest(
        buffer,
        entityType,
        entityFixtures[entityType.qualifiedName]!,
      );
    }

    buffer
      ..writeln('}')
      ..writeln('#endif');
    return buffer.toString();
  }

  void _writeToolTest(
    final StringBuffer buffer,
    final AgentManifestEntry tool,
  ) {
    final typeName = swiftIntentTypeName(tool.qualifiedName);
    final functionName = 'test${typeName}LiveInvocation';
    final typedResult = tool.inlineRuntime?.result;
    buffer
      ..writeln()
      ..writeln('  func $functionName() async throws {')
      ..writeln(
        '    let intent = definitions.intents["$typeName"].makeIntent(${_swiftSampleArguments(tool)})',
      )
      ..writeln('    let result = try await intent.run()');
    if (typedResult != null) {
      buffer
        ..writeln(
          '    let value: ${_swiftTypeForResult(typedResult.type)} = try result.value',
        )
        ..writeln('    _ = value');
    }
    buffer.writeln('  }');
  }

  void _writeEntityTest(
    final StringBuffer buffer,
    final AgentManifestEntityType entityType,
    final AppleAppIntentsTestingEntityFixture fixture,
  ) {
    final typeName = _swiftEntityTypeName(entityType);
    final functionName = 'test${typeName}EntityResolution';
    final escapedIdentifier = escapeSwiftString(fixture.identifier);
    final escapedSearch = escapeSwiftString(fixture.search);
    final escapedTitle = escapeSwiftString(fixture.expectedTitle);
    buffer
      ..writeln()
      ..writeln('  func $functionName() async throws {')
      ..writeln(
        '    let entityDefinition = try XCTUnwrap(definitions.entities["$typeName"])',
      )
      ..writeln(
        '    let byIdentifier = try await entityDefinition.entities(identifiers: ["$escapedIdentifier"])',
      )
      ..writeln('    XCTAssertFalse(byIdentifier.isEmpty)')
      ..writeln(
        '    XCTAssertTrue(byIdentifier.contains { String(describing: \$0.displayRepresentation.title).contains("$escapedTitle") })',
      )
      ..writeln(
        '    let bySearch = try await entityDefinition.entities(matching: "$escapedSearch")',
      )
      ..writeln('    XCTAssertFalse(bySearch.isEmpty)')
      ..writeln(
        '    XCTAssertTrue(bySearch.contains { String(describing: \$0.displayRepresentation.title).contains("$escapedTitle") })',
      )
      ..writeln(
        '    let suggested = try await entityDefinition.suggestedEntities()',
      )
      ..writeln('    XCTAssertFalse(suggested.isEmpty)')
      ..writeln('    let all = try await entityDefinition.allEntities()')
      ..writeln('    XCTAssertFalse(all.isEmpty)')
      ..writeln('    #if os(iOS) || os(macOS)')
      ..writeln(
        '    let spotlight = try await entityDefinition.spotlightQuery("$escapedSearch")',
      )
      ..writeln('    XCTAssertFalse(spotlight.isEmpty)')
      ..writeln('    #endif')
      ..writeln('  }');
  }

  String _swiftSampleArguments(final AgentManifestEntry tool) {
    final values = sampleArguments[tool.qualifiedName] ?? const {};
    final parameters = _swiftParameters(tool);
    final out = <String>[];
    for (final parameter in parameters) {
      final hasValue = values.containsKey(parameter.name);
      if (!hasValue && parameter.required) {
        throw UnsupportedError(
          'Missing sample argument "${parameter.name}" for '
          '${tool.qualifiedName}; AppIntentsTesting live invocation needs '
          'values for required parameters.',
        );
      }
      if (!hasValue) {
        continue;
      }
      out.add(
        '${parameter.variableName}: ${_swiftLiteral(values[parameter.name], parameter.schemaType, tool.qualifiedName, parameter.name)}',
      );
    }
    return out.join(', ');
  }
}

/// Fixture values for generated AppIntentsTesting AppEntity checks.
final class AppleAppIntentsTestingEntityFixture {
  const AppleAppIntentsTestingEntityFixture({
    required this.identifier,
    required this.search,
    required this.expectedTitle,
  });

  /// Identifier expected to resolve through the entity definition.
  final String identifier;

  /// Search text expected to resolve through the entity query and Spotlight.
  final String search;

  /// Display title expected on the resolved sample entity.
  final String expectedTitle;
}

final class _LiveTestParameter {
  const _LiveTestParameter({
    required this.name,
    required this.variableName,
    required this.schemaType,
    required this.required,
  });

  final String name;
  final String variableName;
  final String schemaType;
  final bool required;
}

List<_LiveTestParameter> _swiftParameters(final AgentManifestEntry tool) {
  final properties = tool.inputSchema['properties'];
  if (properties is! Map) {
    return const <_LiveTestParameter>[];
  }
  final rawRequired = tool.inputSchema['required'];
  final required = <String>{
    ...(rawRequired is List ? rawRequired : const <Object?>[])
        .whereType<String>(),
  };
  final out = <_LiveTestParameter>[];
  for (final entry in properties.entries) {
    final name = '${entry.key}';
    final schema = entry.value;
    if (schema is! Map) {
      continue;
    }
    final schemaType = '${schema['type']}';
    if (_swiftTypeForSchema(schemaType) == null) {
      throw UnsupportedError(
        'Apple AppIntentsTesting scaffold supports only primitive '
        'string/integer/number/boolean parameters in ${tool.qualifiedName}; '
        '"$name" has unsupported type "$schemaType".',
      );
    }
    out.add(
      _LiveTestParameter(
        name: name,
        variableName: _swiftIdentifier(name),
        schemaType: schemaType,
        required: required.contains(name),
      ),
    );
  }
  return out;
}

String _swiftLiteral(
  final Object? value,
  final String schemaType,
  final String qualifiedName,
  final String parameterName,
) {
  final invalid = UnsupportedError(
    'Sample argument "$parameterName" for $qualifiedName must match '
    'schema type "$schemaType".',
  );
  return switch (schemaType) {
    'string' when value is String => '"${escapeSwiftString(value)}"',
    'integer' when value is int => '$value',
    'number' when value is num => value.toDouble().toString(),
    'boolean' when value is bool => '$value',
    _ => throw invalid,
  };
}

String? _swiftTypeForSchema(final String type) => switch (type) {
  'string' => 'String',
  'integer' => 'Int',
  'number' => 'Double',
  'boolean' => 'Bool',
  _ => null,
};

String _swiftTypeForResult(final AgentManifestInlineRuntimeResultType type) =>
    switch (type) {
      AgentManifestInlineRuntimeResultType.string => 'String',
      AgentManifestInlineRuntimeResultType.integer => 'Int',
      AgentManifestInlineRuntimeResultType.number => 'Double',
      AgentManifestInlineRuntimeResultType.boolean => 'Bool',
    };

String _swiftEntityTypeName(final AgentManifestEntityType entityType) =>
    '${_swiftTypeBaseName(entityType.qualifiedName)}Entity';

String _swiftTypeBaseName(final String qualifiedName) {
  final parts = qualifiedName.split('_').where((final p) => p.isNotEmpty);
  return parts
      .map((final part) => '${part[0].toUpperCase()}${part.substring(1)}')
      .join();
}

String _swiftIdentifier(final String name) {
  final parts = name
      .split(RegExp('[^A-Za-z0-9]+'))
      .where((final p) => p.isNotEmpty);
  if (parts.isEmpty) {
    return 'value';
  }
  final first = parts.first;
  final rest = parts
      .skip(1)
      .map((final part) => '${part[0].toUpperCase()}${part.substring(1)}');
  final candidate = first + rest.join();
  final identifier = RegExp('^[A-Za-z_]').hasMatch(candidate)
      ? candidate
      : 'value$candidate';
  return _swiftReservedWords.contains(identifier)
      ? '`$identifier`'
      : identifier;
}

