import '../agent_manifest.dart';
import 'emitter_utils.dart';

/// Emits Swift App Intents + shortcuts provider for iOS and macOS.
///
/// Turns manifest rows into compile-time Swift for `Runner/Generated/`.
/// (Supersedes the removed `intentcall_apple` sparse-manifest generator.)
final class AppleSwiftAppIntentsEmitter {
  const AppleSwiftAppIntentsEmitter({this.fallbackProtocolScheme});

  final String? fallbackProtocolScheme;

  String emit(final AgentManifest manifest) {
    final protocolScheme = fallbackProtocolScheme ?? manifest.protocolScheme;
    final intentTools = manifest.tools
        .where(
          (final tool) => tool.surfaces.includes(
            AgentManifestSurface.appleAppIntents,
            defaultValue: false,
          ),
        )
        .toList();
    final emitEntities = _manifestEmitsAppleEntities(manifest);
    final emitSpotlight = _manifestEmitsAppleSpotlight(manifest);
    final bridgeProtocolScheme =
        intentTools.any(
          (final tool) =>
              tool.dispatchMode == AgentManifestDispatchMode.openApp,
        )
        ? protocolScheme
        : null;
    final buffer = StringBuffer()
      ..writeln('// Generated by intentcall_platform — do not edit by hand.')
      ..writeln('import AppIntents')
      ..writeln('import Foundation')
      ..writeln('import intentcall_platform_apple');
    if (emitSpotlight) {
      buffer.writeln('import CoreSpotlight');
    }
    buffer
      ..writeln('#if canImport(UIKit)')
      ..writeln('import UIKit')
      ..writeln('#elseif canImport(AppKit)')
      ..writeln('import AppKit')
      ..writeln('#endif')
      ..writeln();

    final shortcutLines = <String>[];
    for (final tool in intentTools) {
      final inlineRuntime = _appleInlineRuntime(tool);
      final typeName = swiftIntentTypeName(tool.qualifiedName);
      final title = escapeSwiftString(
        tool.description.isEmpty
            ? humanizeAgentName(tool.name)
            : tool.description,
      );
      final phrase = escapeSwiftString(humanizeAgentName(tool.name));
      final parameters = _swiftParameters(tool);
      final argumentLines = _swiftArgumentLines(parameters);
      final opensApp = tool.dispatchMode == AgentManifestDispatchMode.openApp;
      final typedResult = tool.inlineRuntime?.result;
      final mode = inlineRuntime == null
          ? opensApp
                ? _AppleIntentMode.openApp
                : _AppleIntentMode.queueOnly
          : _AppleIntentMode.nativeInline;
      buffer
        ..writeln('@available(iOS 16.0, macOS 13.0, *)')
        ..writeln('struct $typeName: AppIntent {')
        ..writeln('  static var title: LocalizedStringResource = "$title"')
        ..write(_swiftModeProperties(mode))
        ..write(_swiftExecutionTargetProperty(mode))
        ..writeln();
      for (final parameter in parameters) {
        buffer
          ..writeln(
            '  @Parameter(title: "${escapeSwiftString(parameter.title)}")',
          )
          ..writeln('  var ${parameter.variableName}: ${parameter.swiftType}')
          ..writeln();
      }
      buffer
        ..writeln(
          '  func perform() async throws -> ${_swiftPerformResultType(typedResult)} {',
        )
        ..writeln('    var arguments: [String: Any] = [:]');
      for (final line in argumentLines) {
        buffer.writeln('    $line');
      }
      if (inlineRuntime == null) {
        final schemeArgument = opensApp && bridgeProtocolScheme != null
            ? ', fallbackProtocolScheme: ${_swiftOptionalString(bridgeProtocolScheme)}'
            : '';
        buffer
          ..writeln(
            '    let invocationId = await IntentCallNativeBridge.enqueue(qualifiedName: "${escapeSwiftString(tool.qualifiedName)}", arguments: arguments, openApp: $opensApp$schemeArgument)',
          )
          ..writeln(
            r'    return .result(dialog: IntentDialog("Queued invocation \(invocationId) for app dispatch."))',
          );
      } else {
        if (typedResult == null) {
          buffer
            ..writeln('    do {')
            ..writeln(
              '      let inlineResult = try await IntentCallAppleInlineRuntime.perform(qualifiedName: "${escapeSwiftString(tool.qualifiedName)}", arguments: arguments)',
            )
            ..writeln(
              '      return .result(dialog: IntentDialog(stringLiteral: inlineResult.dialog))',
            )
            ..writeln('    } catch let error as IntentCallInlineRuntimeError {')
            ..writeln(
              '      return .result(dialog: IntentDialog(stringLiteral: error.description))',
            )
            ..writeln('    } catch {')
            ..writeln(
              r'      return .result(dialog: IntentDialog("Inline runtime failed: \(error.localizedDescription)"))',
            )
            ..writeln('    }');
        } else {
          final swiftType = _swiftTypeForResult(typedResult.type);
          buffer
            ..writeln(
              '    let inlineResult = try await IntentCallAppleInlineRuntime.perform(qualifiedName: "${escapeSwiftString(tool.qualifiedName)}", arguments: arguments)',
            )
            ..writeln(
              '    let value = try IntentCallAppleInlineRuntime.typedValue(inlineResult, as: $swiftType.self, qualifiedName: "${escapeSwiftString(tool.qualifiedName)}")',
            )
            ..writeln(
              '    return .result(value: value, dialog: IntentDialog(stringLiteral: inlineResult.dialog))',
            );
        }
      }
      buffer
        ..writeln('  }')
        ..writeln('}')
        ..writeln();
      if (tool.surfaces.includes(
        AgentManifestSurface.appleAppShortcuts,
        defaultValue: false,
      )) {
        shortcutLines.add(
          '    AppShortcut(intent: $typeName(), phrases: ["\\(.applicationName) $phrase"])',
        );
      }
    }

    for (final entityType in manifest.entityTypes) {
      if (emitEntities) {
        buffer.write(
          _swiftEntityType(entityType, includeSpotlight: emitSpotlight),
        );
      }
    }

    buffer
      ..writeln('@available(iOS 16.0, macOS 13.0, *)')
      ..writeln('struct IntentCallShortcutsProvider: AppShortcutsProvider {')
      ..writeln('  static var appShortcuts: [AppShortcut] {');
    if (shortcutLines.isEmpty) {
      buffer.writeln('    return []');
    } else {
      shortcutLines.forEach(buffer.writeln);
    }
    buffer
      ..writeln('  }')
      ..writeln('}')
      ..writeln();
    if (intentTools.any(
      (final tool) =>
          tool.dispatchMode == AgentManifestDispatchMode.inlineRuntime,
    )) {
      buffer.write(_swiftInlineRuntimeSupport());
    }
    if (emitEntities) {
      buffer
        ..writeln()
        ..write(
          _swiftEntitySnapshotSupport(
            manifest.entityTypes,
            protocolScheme,
            includeSpotlight: emitSpotlight,
          ),
        );
    }
    return buffer.toString();
  }
}

bool _manifestEmitsAppleEntities(final AgentManifest manifest) {
  if (manifest.entityTypes.isEmpty) {
    return false;
  }
  final tools = manifest.tools.toList();
  if (tools.isEmpty) {
    return true;
  }
  return tools.any(
    (final tool) => tool.surfaces.includes(
      AgentManifestSurface.appleEntities,
      defaultValue: false,
    ),
  );
}

bool _manifestEmitsAppleSpotlight(final AgentManifest manifest) {
  if (!_manifestEmitsAppleEntities(manifest)) {
    return false;
  }
  final tools = manifest.tools.toList();
  if (tools.isEmpty) {
    return true;
  }
  return tools.any(
    (final tool) => tool.surfaces.includes(
      AgentManifestSurface.appleSpotlight,
      defaultValue: false,
    ),
  );
}

AgentManifestAppleInlineRuntime? _appleInlineRuntime(
  final AgentManifestEntry tool,
) {
  if (tool.dispatchMode != AgentManifestDispatchMode.inlineRuntime) {
    return null;
  }
  final inlineRuntime = tool.inlineRuntime;
  if (inlineRuntime == null) {
    throw UnsupportedError(
      'Apple inlineRuntime dispatch for ${tool.qualifiedName} requires '
      'inlineRuntime metadata.',
    );
  }
  final apple = inlineRuntime.platforms.apple;
  if (apple == null) {
    throw UnsupportedError(
      'Apple inlineRuntime dispatch for ${tool.qualifiedName} requires '
      'inlineRuntime.platforms.apple.',
    );
  }
  if (inlineRuntime.kind ==
      AgentManifestInlineRuntimeKind.dartExtensionInline) {
    throw UnsupportedError(
      'Apple dartExtensionInline dispatch is experimental and not supported '
      'in the main app emitter for ${tool.qualifiedName}. Use '
      'AppleDartExtensionInlineEmitter(enableExperimental: true) only for a '
      'proof extension target with FlutterEngine bootstrap proof, plugin '
      'audit, App Group or IPC storage, timeout/memory limits, and live OS '
      'invocation evidence.',
    );
  }
  if (inlineRuntime.kind != AgentManifestInlineRuntimeKind.nativeInline) {
    throw UnsupportedError(
      'Apple inlineRuntime kind ${inlineRuntime.kind.name} is not supported '
      'for ${tool.qualifiedName}.',
    );
  }
  if (apple.target != AgentManifestAppleInlineRuntimeTarget.mainApp) {
    throw UnsupportedError(
      'Apple nativeInline currently supports target "mainApp" only for '
      '${tool.qualifiedName}; appIntentsExtension remains future proof work.',
    );
  }
  return apple;
}

String _swiftEntityType(
  final AgentManifestEntityType entityType, {
  required final bool includeSpotlight,
}) {
  final entityTypeName = _swiftEntityTypeName(entityType);
  final queryTypeName = '${entityTypeName}Query';
  final openIntentTypeName = _swiftOpenEntityIntentTypeName(entityType);
  final displayName = escapeSwiftString(entityType.displayName);
  final description = escapeSwiftString(
    entityType.description.isEmpty
        ? 'Open ${entityType.displayName}'
        : entityType.description,
  );
  final qualifiedName = escapeSwiftString(entityType.qualifiedName);
  final idKey = escapeSwiftString(entityType.idKey);
  final titleKey = escapeSwiftString(entityType.titleKey);
  final subtitleKey = escapeSwiftString(entityType.subtitleKey);
  final keywordsKey = escapeSwiftString(entityType.keywordsKey);
  final defaultQueryLimit = entityType.defaultQueryLimit;
  final entityProtocols = includeSpotlight
      ? 'AppEntity, IndexedEntity'
      : 'AppEntity';
  final queryProtocols = includeSpotlight
      ? 'EntityStringQuery, IndexedEntityQuery'
      : 'EntityStringQuery';
  final buffer = StringBuffer()
    ..writeln('@available(iOS 18.0, macOS 15.0, *)')
    ..writeln('struct $entityTypeName: $entityProtocols {')
    ..writeln(
      '  static var typeDisplayRepresentation: TypeDisplayRepresentation = "$displayName"',
    )
    ..writeln('  static var defaultQuery = $queryTypeName()')
    ..writeln()
    ..writeln('  let id: String')
    ..writeln('  let title: String')
    ..writeln('  let subtitle: String')
    ..writeln('  let keywords: [String]')
    ..writeln()
    ..writeln(
      '  init(id: String, title: String? = nil, subtitle: String = "", keywords: [String] = []) {',
    )
    ..writeln('    self.id = id')
    ..writeln('    self.title = title ?? id')
    ..writeln('    self.subtitle = subtitle')
    ..writeln('    self.keywords = keywords')
    ..writeln('  }')
    ..writeln()
    ..writeln('  init(snapshot: [String: Any]) {')
    ..writeln(
      '    self.id = IntentCallNativeEntitySnapshotStore.string(snapshot["$idKey"]) ?? ""',
    )
    ..writeln(
      '    self.title = IntentCallNativeEntitySnapshotStore.string(snapshot["$titleKey"]) ?? self.id',
    )
    ..writeln(
      '    self.subtitle = IntentCallNativeEntitySnapshotStore.string(snapshot["$subtitleKey"]) ?? ""',
    )
    ..writeln(
      '    self.keywords = IntentCallNativeEntitySnapshotStore.strings(snapshot["$keywordsKey"])',
    )
    ..writeln('  }')
    ..writeln()
    ..writeln('  var displayRepresentation: DisplayRepresentation {')
    ..writeln('    if !subtitle.isEmpty {')
    ..writeln(
      r'      return DisplayRepresentation(title: "\(title)", subtitle: "\(subtitle)")',
    )
    ..writeln('    }')
    ..writeln(r'    return DisplayRepresentation(title: "\(title)")')
    ..writeln('  }')
    ..writeln('}')
    ..writeln()
    ..writeln('@available(iOS 18.0, macOS 15.0, *)')
    ..writeln('struct $queryTypeName: $queryProtocols {')
    ..writeln(
      '  func entities(for identifiers: [String]) async throws -> [$entityTypeName] {',
    )
    ..writeln(
      '    IntentCallNativeEntitySnapshotStore.entities(entityType: "$qualifiedName", identifiers: identifiers, idKey: "$idKey", limit: nil).map($entityTypeName.init(snapshot:))',
    )
    ..writeln('  }')
    ..writeln()
    ..writeln('  func suggestedEntities() async throws -> [$entityTypeName] {')
    ..writeln(
      '    IntentCallNativeEntitySnapshotStore.suggested(entityType: "$qualifiedName", limit: $defaultQueryLimit).map($entityTypeName.init(snapshot:))',
    )
    ..writeln('  }')
    ..writeln()
    ..writeln(
      '  func entities(matching string: String) async throws -> [$entityTypeName] {',
    )
    ..writeln(
      '    IntentCallNativeEntitySnapshotStore.search(entityType: "$qualifiedName", query: string, titleKey: "$titleKey", subtitleKey: "$subtitleKey", keywordsKey: "$keywordsKey", limit: $defaultQueryLimit).map($entityTypeName.init(snapshot:))',
    )
    ..writeln('  }');
  if (includeSpotlight) {
    buffer
      ..writeln()
      ..writeln('  @available(iOS 27.0, macOS 27.0, *)')
      ..writeln(
        '  func reindexEntities(for identifiers: [String], indexDescription: CSSearchableIndexDescription) async throws {',
      )
      ..writeln(
        '    let entities = IntentCallNativeEntitySnapshotStore.entities(entityType: "$qualifiedName", identifiers: identifiers, idKey: "$idKey", limit: nil).map($entityTypeName.init(snapshot:))',
      )
      ..writeln('    guard !entities.isEmpty else { return }')
      ..writeln(
        '    try await CSSearchableIndex.default().indexAppEntities(entities)',
      )
      ..writeln('  }')
      ..writeln()
      ..writeln('  @available(iOS 27.0, macOS 27.0, *)')
      ..writeln(
        '  func reindexAllEntities(indexDescription: CSSearchableIndexDescription) async throws {',
      )
      ..writeln(
        '    let entities = IntentCallNativeEntitySnapshotStore.snapshots(entityType: "$qualifiedName").map($entityTypeName.init(snapshot:))',
      )
      ..writeln('    guard !entities.isEmpty else { return }')
      ..writeln(
        '    try await CSSearchableIndex.default().indexAppEntities(entities)',
      )
      ..writeln('  }');
  }
  buffer
    ..writeln('}')
    ..writeln()
    ..writeln('@available(iOS 18.0, macOS 15.0, *)')
    ..writeln('struct $openIntentTypeName: OpenIntent {')
    ..writeln('  static var title: LocalizedStringResource = "$description"')
    ..write(_swiftModeProperties(_AppleIntentMode.openApp))
    ..write(_swiftExecutionTargetProperty(_AppleIntentMode.openApp))
    ..writeln()
    ..writeln('  @Parameter(title: "$displayName")')
    ..writeln('  var target: $entityTypeName')
    ..writeln()
    ..writeln('  func perform() async throws -> some IntentResult {')
    ..writeln(
      '    let openId = await IntentCallNativeEntitySnapshotStore.recordOpen(entityType: "$qualifiedName", id: target.id)',
    )
    ..writeln(
      r'    return .result(dialog: IntentDialog("Queued entity \(openId) for app dispatch."))',
    )
    ..writeln('  }')
    ..writeln('}')
    ..writeln();
  return buffer.toString();
}

String _swiftEntitySnapshotSupport(
  final Iterable<AgentManifestEntityType> entityTypes,
  final String? protocolScheme, {
  required final bool includeSpotlight,
}) {
  final buffer = StringBuffer();
  if (includeSpotlight) {
    buffer
      ..writeln('@available(iOS 18.0, macOS 15.0, *)')
      ..writeln('enum IntentCallAppEntityIndexer {')
      ..writeln('  static func indexAppEntities() async throws {');
    for (final entityType in entityTypes) {
      final entityTypeName = _swiftEntityTypeName(entityType);
      final variableName = _swiftLocalEntityVariableName(entityTypeName);
      buffer
        ..writeln(
          '    let $variableName = IntentCallNativeEntitySnapshotStore.snapshots(entityType: "${escapeSwiftString(entityType.qualifiedName)}").map($entityTypeName.init(snapshot:))',
        )
        ..writeln('    if !$variableName.isEmpty {')
        ..writeln(
          '      try await CSSearchableIndex.default().indexAppEntities($variableName)',
        )
        ..writeln('    }');
    }
    buffer
      ..writeln('  }')
      ..writeln()
      ..writeln(
        '  static func deleteAppEntities(entityType: String, ids: [String]) async throws {',
      )
      ..writeln(r'    let identifiers = ids.map { "\(entityType):\($0)" }')
      ..writeln('    guard !identifiers.isEmpty else { return }')
      ..writeln(
        '    try await withCheckedThrowingContinuation { (continuation: CheckedContinuation<Void, Error>) in',
      )
      ..writeln(
        '      CSSearchableIndex.default().deleteSearchableItems(withIdentifiers: identifiers) { error in',
      )
      ..writeln('        if let error {')
      ..writeln('          continuation.resume(throwing: error)')
      ..writeln('        } else {')
      ..writeln('          continuation.resume()')
      ..writeln('        }')
      ..writeln('      }')
      ..writeln('    }')
      ..writeln('  }')
      ..writeln()
      ..writeln('  static func reindexAllEntities() async throws {')
      ..writeln('    try await indexAppEntities()')
      ..writeln('  }')
      ..writeln('}')
      ..writeln();
  }
  buffer
    ..writeln('enum IntentCallGeneratedEntityConfig {')
    ..writeln('  static func configure() {')
    ..writeln(
      '    IntentCallNativeEntitySnapshotStore.fallbackScheme = ${_swiftOptionalString(protocolScheme)}',
    )
    ..writeln('  }')
    ..writeln('}')
    ..writeln(
      'private let _intentCallGeneratedEntityConfig = IntentCallGeneratedEntityConfig.configure()',
    );
  if (includeSpotlight) {
    buffer
      ..writeln()
      ..writeln('@available(iOS 18.0, macOS 15.0, *)')
      ..writeln('private final class IntentCallEntitySnapshotReindexObserver {')
      ..writeln('  init() {')
      ..writeln('    NotificationCenter.default.addObserver(')
      ..writeln(
        '      forName: IntentCallNativeEntitySnapshotStore.snapshotsDidChangeNotification,',
      )
      ..writeln('      object: nil,')
      ..writeln('      queue: nil')
      ..writeln('    ) { _ in')
      ..writeln('      Task {')
      ..writeln(
        '        try? await IntentCallAppEntityIndexer.indexAppEntities()',
      )
      ..writeln('      }')
      ..writeln('    }')
      ..writeln('  }')
      ..writeln('}')
      ..writeln(
        'private let _intentCallEntitySnapshotReindexObserver = IntentCallEntitySnapshotReindexObserver()',
      );
  }
  return buffer.toString();
}

enum _AppleIntentMode { nativeInline, openApp, queueOnly }

String _swiftModeProperties(final _AppleIntentMode mode) {
  final supportedModes = switch (mode) {
    _AppleIntentMode.openApp => '.foreground(.immediate)',
    _AppleIntentMode.nativeInline ||
    _AppleIntentMode.queueOnly => '.background',
  };
  final buffer = StringBuffer()
    ..writeln('  @available(iOS 26.0, macOS 26.0, *)')
    ..writeln('  static var supportedModes: IntentModes { $supportedModes }');
  if (mode != _AppleIntentMode.nativeInline) {
    buffer.writeln(
      '  static var openAppWhenRun: Bool = ${mode == _AppleIntentMode.openApp ? 'true' : 'false'}',
    );
  }
  return buffer.toString();
}

String _swiftExecutionTargetProperty(final _AppleIntentMode mode) {
  final target = switch (mode) {
    _AppleIntentMode.nativeInline => '.main',
    _AppleIntentMode.openApp || _AppleIntentMode.queueOnly => '.default',
  };
  return '''
  @available(iOS 27.0, macOS 27.0, *)
  static var allowedExecutionTargets: IntentExecutionTargets { $target }
''';
}

String _swiftPerformResultType(final AgentManifestInlineRuntimeResult? result) {
  if (result == null) {
    return 'some IntentResult';
  }
  return 'some IntentResult & ReturnsValue<${_swiftTypeForResult(result.type)}> & ProvidesDialog';
}

String _swiftInlineRuntimeSupport() => r'''
struct IntentCallInlineRuntimeResult {
  let dialog: String
  let value: Any?

  init(dialog: String = "Completed inline runtime invocation.", value: Any? = nil) {
    self.dialog = dialog
    self.value = value
  }
}

enum IntentCallInlineRuntimeError: Error, CustomStringConvertible {
  case missingHandler(String)
  case handlerFailed(String)
  case invalidTypedResult(String, String)

  var description: String {
    switch self {
    case .missingHandler(let qualifiedName):
      return "No native inline handler registered for \(qualifiedName)."
    case .handlerFailed(let message):
      return "Inline runtime failed: \(message)"
    case .invalidTypedResult(let qualifiedName, let typeName):
      return "Inline runtime for \(qualifiedName) did not return \(typeName)."
    }
  }
}

typealias IntentCallAppleInlineRuntimeHandler = @Sendable ([String: Any]) async throws -> IntentCallInlineRuntimeResult

enum IntentCallAppleInlineRuntime {
  private static let lock = NSObject()
  private nonisolated(unsafe) static var handlers: [String: IntentCallAppleInlineRuntimeHandler] = [:]

  static func register(qualifiedName: String, handler: @escaping IntentCallAppleInlineRuntimeHandler) {
    objc_sync_enter(lock)
    defer { objc_sync_exit(lock) }
    handlers[qualifiedName] = handler
  }

  static func perform(qualifiedName: String, arguments: [String: Any]) async throws -> IntentCallInlineRuntimeResult {
    objc_sync_enter(lock)
    let handler = handlers[qualifiedName]
    objc_sync_exit(lock)
    guard let handler else {
      throw IntentCallInlineRuntimeError.missingHandler(qualifiedName)
    }
    do {
      return try await handler(arguments)
    } catch let error as IntentCallInlineRuntimeError {
      throw error
    } catch {
      throw IntentCallInlineRuntimeError.handlerFailed(error.localizedDescription)
    }
  }

  static func typedValue<T>(_ result: IntentCallInlineRuntimeResult, as type: T.Type, qualifiedName: String) throws -> T {
    guard let raw = result.value else {
      throw IntentCallInlineRuntimeError.invalidTypedResult(qualifiedName, String(describing: T.self))
    }
    if let value = raw as? T {
      return value
    }
    if T.self == Int.self, let value = raw as? NSNumber {
      return value.intValue as! T
    }
    if T.self == Double.self, let value = raw as? NSNumber {
      return value.doubleValue as! T
    }
    if T.self == Bool.self, let value = raw as? NSNumber {
      return value.boolValue as! T
    }
    throw IntentCallInlineRuntimeError.invalidTypedResult(qualifiedName, String(describing: T.self))
  }
}
''';

String _swiftOptionalString(final String? value) {
  final trimmed = value?.trim() ?? '';
  if (trimmed.isEmpty) {
    return 'nil';
  }
  requireProtocolScheme(trimmed, artifact: 'Apple fallback URL');
  return '"${escapeSwiftString(trimmed)}"';
}

final class _SwiftParameter {
  const _SwiftParameter({
    required this.name,
    required this.variableName,
    required this.title,
    required this.swiftType,
    required this.required,
  });

  final String name;
  final String variableName;
  final String title;
  final String swiftType;
  final bool required;
}

List<_SwiftParameter> _swiftParameters(final AgentManifestEntry tool) {
  final properties = tool.inputSchema['properties'];
  if (properties is! Map) {
    return const <_SwiftParameter>[];
  }
  final rawRequired = tool.inputSchema['required'];
  final required = <String>{
    ...(rawRequired is List ? rawRequired : const <Object?>[])
        .whereType<String>(),
  };
  final out = <_SwiftParameter>[];
  for (final entry in properties.entries) {
    final name = '${entry.key}';
    final schema = entry.value;
    if (schema is! Map) {
      continue;
    }
    final schemaType = '${schema['type']}';
    final baseType = _swiftTypeFor(schemaType);
    if (baseType == null) {
      throw UnsupportedError(
        'Apple App Intents support only primitive string/integer/number/boolean '
        'parameters in ${tool.qualifiedName}; "$name" has unsupported type '
        '"$schemaType".',
      );
    }
    final isRequired = required.contains(name);
    out.add(
      _SwiftParameter(
        name: name,
        variableName: _swiftIdentifier(name),
        title: humanizeAgentName(name),
        swiftType: isRequired ? baseType : '$baseType?',
        required: isRequired,
      ),
    );
  }
  return out;
}

List<String> _swiftArgumentLines(final List<_SwiftParameter> parameters) {
  final out = <String>[];
  for (final parameter in parameters) {
    if (parameter.required) {
      out.add(
        'arguments["${escapeSwiftString(parameter.name)}"] = ${parameter.variableName}',
      );
    } else {
      out.add(
        'if let value = ${parameter.variableName} { arguments["${escapeSwiftString(parameter.name)}"] = value }',
      );
    }
  }
  return out;
}

String? _swiftTypeFor(final String type) => switch (type) {
  'string' => 'String',
  'integer' => 'Int',
  'number' => 'Double',
  'boolean' => 'Bool',
  _ => null,
};

String _swiftTypeForResult(final AgentManifestInlineRuntimeResultType type) =>
    switch (type) {
      AgentManifestInlineRuntimeResultType.string => 'String',
      AgentManifestInlineRuntimeResultType.integer => 'Int',
      AgentManifestInlineRuntimeResultType.number => 'Double',
      AgentManifestInlineRuntimeResultType.boolean => 'Bool',
    };

String _swiftEntityTypeName(final AgentManifestEntityType entityType) =>
    '${_swiftTypeBaseName(entityType.qualifiedName)}Entity';

String _swiftLocalEntityVariableName(final String entityTypeName) {
  if (entityTypeName.isEmpty) {
    return 'entities';
  }
  return '${entityTypeName[0].toLowerCase()}${entityTypeName.substring(1)}s';
}

String _swiftOpenEntityIntentTypeName(
  final AgentManifestEntityType entityType,
) {
  final baseName = _swiftTypeBaseName(entityType.qualifiedName);
  final withoutNamespace = baseName.startsWith('App') && baseName.length > 3
      ? baseName.substring(3)
      : baseName;
  return 'AppOpen${withoutNamespace}EntityIntent';
}

String _swiftTypeBaseName(final String qualifiedName) {
  final parts = qualifiedName.split('_').where((final p) => p.isNotEmpty);
  return parts
      .map((final part) => '${part[0].toUpperCase()}${part.substring(1)}')
      .join();
}

String _swiftIdentifier(final String name) {
  final parts = name
      .split(RegExp('[^A-Za-z0-9]+'))
      .where((final p) => p.isNotEmpty);
  if (parts.isEmpty) {
    return 'value';
  }
  final first = parts.first;
  final rest = parts
      .skip(1)
      .map((final part) => '${part[0].toUpperCase()}${part.substring(1)}');
  final candidate = first + rest.join();
  final identifier = RegExp('^[A-Za-z_]').hasMatch(candidate)
      ? candidate
      : 'value$candidate';
  return _swiftReservedWords.contains(identifier)
      ? '`$identifier`'
      : identifier;
}

